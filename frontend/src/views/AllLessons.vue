import {useMvpStore} from "@/stores/mvp";

const week = ref(10)
const date = ref(getMonday(new Date()).toLocaleDateString());
const days = [
  '(пн)',
  '(вт)',
  '(ср)',
  '(чт)',
  '(пт)',
